package com.pojo;

//renyuan表的pojo类
public class Renyuandata {
	private String renid;
	private String renname;
	private String roleid;
	private String depid;
	private String renstate;
	public String getRenid() {
		return renid;
	}
	public void setRenid(String renid) {
		this.renid = renid;
	}
	public String getRenname() {
		return renname;
	}
	public void setRenname(String renname) {
		this.renname = renname;
	}
	public String getRoleid() {
		return roleid;
	}
	public void setRoleid(String roleid) {
		this.roleid = roleid;
	}
	public String getDepid() {
		return depid;
	}
	public void setDepid(String depid) {
		this.depid = depid;
	}
	public String getRenstate() {
		return renstate;
	}
	public void setRenstate(String renstate) {
		this.renstate = renstate;
	}
	
}
